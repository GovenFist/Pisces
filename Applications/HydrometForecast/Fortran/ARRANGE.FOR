50	I=I-1
	IF(I.EQ.35)THEN
		DO 15 K=1,35
		WRITE(2,2)A(K),N(K)
2	FORMAT(A,1X,I2)
15		CONTINUE
		END IF
	IF(I.NE.35)THEN
		DO 16 K=1,I
